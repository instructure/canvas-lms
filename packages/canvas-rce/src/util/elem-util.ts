/**
 * Utility function to check if a node is Text in a typesafe manner to allow TypeScript type narrowing.
 *
 * @param n
 */
export function isTextNode(n: Node | null | undefined): n is Text {
  return n?.nodeType === Node.TEXT_NODE
}
