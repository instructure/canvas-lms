import React from 'react'
import {SimpleSelect} from '@instructure/ui-simple-select'

interface StatePickerProps {
  state: string
  size?: 'small' | 'medium' | 'large'
  width?: string
  onChange: (state: string) => void
}

const StatePicker = ({state, size, width, onChange}: StatePickerProps) => {
  const renderStates = () => {
    const states: Record<string, string> = {
      '--': '',
      Alabama: 'AL',
      Alaska: 'AK',
      Arizona: 'AZ',
      Arkansas: 'AR',
      California: 'CA',
      Colorado: 'CO',
      Connecticut: 'CT',
      Delaware: 'DE',
      Florida: 'FL',
      Georgia: 'GA',
      Hawaii: 'HI',
      Idaho: 'ID',
      Illinois: 'IL',
      Indiana: 'IN',
      Iowa: 'IA',
      Kansas: 'KS',
      Kentucky: 'KY',
      Louisiana: 'LA',
      Maine: 'ME',
      Maryland: 'MD',
      Massachusetts: 'MA',
      Michigan: 'MI',
      Minnesota: 'MN',
      Mississippi: 'MS',
      Missouri: 'MO',
      Montana: 'MT',
      Nebraska: 'NE',
      Nevada: 'NV',
      'New Hampshire': 'NH',
      'New Jersey': 'NJ',
      'New Mexico': 'NM',
      'New York': 'NY',
      'North Carolina': 'NC',
      'North Dakota': 'ND',
      Ohio: 'OH',
      Oklahoma: 'OK',
      Oregon: 'OR',
      Pennsylvania: 'PA',
      'Rhode Island': 'RI',
      'South Carolina': 'SC',
      'South Dakota': 'SD',
      Tennessee: 'TN',
      Texas: 'TX',
      Utah: 'UT',
      Vermont: 'VT',
      Virginia: 'VA',
      Washington: 'WA',
      'West Virginia': 'WV',
      Wisconsin: 'WI',
      Wyoming: 'WY',
    }
    return Object.keys(states).map(st => (
      <SimpleSelect.Option id={st} key={st} value={states[st]}>
        {st}
      </SimpleSelect.Option>
    ))
  }

  return (
    <SimpleSelect
      size={size}
      width={width}
      renderLabel="State"
      value={state}
      onChange={(_e, data) => onChange(data.value as string)}
    >
      {renderStates()}
    </SimpleSelect>
  )
}

export default StatePicker
